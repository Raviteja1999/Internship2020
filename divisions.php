                    <strong><span style="color:#b22222;">DIVISION OF CROP PRODUCTION</span></strong></p>
                    
                <p>
                    Development of appropriate agro-techniques to enhance the productivity of tobacco and tobacco based cropping systems on a sustainable basis. Suitable Good Agricultural practices (GAPs) were translated to the farming community of different tobacco domains by developing new concepts and approaches for sustainable tobacco production. Technologies were developed and translated to the farming community on water use efficient micro-irrigation systems for nursery production, poly tray seedling production, integrated nutrient management, organic farming, integrated farming systems, region specific fertilizer recommendation, watershed management for SLS,  Drip fertigation for irrigated Alfisols, integrated weed management and alternative uses of tobacco. Since, flue curing costs about 30% of cost of production in FCV tobacco production, technologies for fuel saving viz. low profile barn, insulation with thermocole or rock-wool or straw-mit, redesigning the arrangement of barn flue pipes, modification of the  barn furnace for fuel saving and reducing the cost of curing by use of  alternative fuels to wood and coal were developed. Research work is under progress for integrated weed management including Orobanche, strategies to mitigate false ripening, climate resilient technologies etc., Since, tobacco is a high value cash crop; only cropping systems rather than a single crop can be next best viable alternative option to tobacco hence, Cropping systems/ crops for different agro-ecological sub-regions have been developed and recommended.</P>

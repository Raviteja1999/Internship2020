                    <strong><span style="color:#b22222;">BLACK SOIL FARM, KATHERU (RAJAHMUNDRY) </span></strong></p>
                    
                    <p>
                    The experimental farm attached to CTRI, Rajahmundry is situated at Katheru village, a hamlet of Rajahmundry Corporation. Farm Superintendent is the in-charge of the farm who works under the advice of Heads of divisions. The farm is spread over an area of 47.38 hectares adjacent to the river Godavari. The Fam soil is heavy black soil classified as vertisol. The Farm caters to the needs of Crop Improvement, Crop Production, Crop Chemistry &amp; Soil Science and Crop Protection divisions for conducting various experiments. It is equipped with tractor and bullock drawn agriculture implements for cultivation of different crops, such as FCV, Burley, Natu Tobacco; Blackgram, Greengram, Horsegram, Chickpea, Maize, Soybean, Pigeonpea, Mustard, Coriander and fodder jowar.</p>

                <p>
                    The experimental farm has &lsquo;A&rsquo; class Meteorological Observatory which operates under the Indian Meteorological Department, Pune. Observations on atmospheric temperature, soil temperature, total rainfall, No. of rainy days, Relative humidity, evaporation, sunshine hours, wind velocity, wind direction, dew fall and lysimeter readings are recorded daily. The weather data is also supplied to different Govt. and Non-governmental organizations. </p>


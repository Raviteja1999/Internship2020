                    <h3 style="text-align: left; "><span style="color:#b22222;">DIVISION OF CROP IMPROVEMENT</span></h3>
                <br>
                <p>
                    The Division of Crop Improvement takes the responsibility of the crop improvement research needs of all types of tobacco grown in India. The Division comprises two disciplines, Plant Genetics and Breeding and Biotechnology. The scientists of the Division employ conventional breeding with biotechnology methods for the improvement of Indian tobacco cultivars in terms of productivity, physical & chemical quality, trade preference, resistance to biotic & abiotic stresses, reducing harmful substances like nicotine, tar, carbon monoxide, tobacco specific nitrosamines (TSNA), etc. The materials developed by the Division form the basic material for carrying out research by other disciplines. Also, germplasm maintenance and enhancement, cytogenetical, genetical and molecular studies on tobacco are the important basic and strategic areas of research undertaken at the division. Production and distribution of quality seed to farmers is another key responsibility of the Division. In addition, the Division assists in the smooth functioning of the All India Network Research Project on Tobacco (AINPT) in general and crop improvement in particular. </p>

                    <p>
                This Division have so far instrumental in developing 98 varieties in all types of tobacco for cultivation throughout the country. All the ruling FCV tobacco varieties cultivated in India viz., Hema, VT-1158, Siri, N-98 and CTRI Sulakhana in black soils and southern light soils of Andhra Pradesh; Kanchan in Northern light soils of A.P; Kanchan, Rathna and FCH-222 in Karnataka light soils are released by the Division. Chewing varieties viz., Lichchavi, Pusa Tobacco-76, Bhagyalakshmi, Meenakshi, Meenakshi (CR), Abirami, Kaviri and Manasi; Rustica varieties viz., DD-437, Darla and Torsa; Burley variety, Banket A-1; Cheroot varieties viz., Natu Special, Bhairavi and Lanka Special are some of the prominent non-FCV tobacco varieties released. Tobacco Mosaic Virus resistant varieties like VT-1158, Godavari Special and Banket A-1; Black-shank resistant varieties like CM-12(KA), Kanchan and Bhavya; Powdery Mildew resistant variety Swarna and caterpillar resistant chewing variety, Meenakshi CR etc. are few of the resistant cultivars released by the Division. Lines resistant to tobacco caterpillar (Spodoptera litura), tobacco aphid (Myzus nicotianae), Budworm (Helicoverpa armigera) and root-knot nematodes were also developed through interspecific hybridization and biotechnological approaches. In view of the health problems associated with tobacco, a number of improved lines having lower levels of harmful substances (Tar and TSNA - Tobacco Specific Nitrosamines) have been developed. A low tar yielding FCV variety, LT- Kanchan was released for NLS area. Also, hybrids having high yield potential along with desirable leaf quality viz. FCV hybrid, CH-1 for NLS and CH-3 for KLS, chewing tobacco hybrid, Kamatchi were released for the benefit of the farming community. Due to the concerted efforts of the scientists, the yield potential of FCV varieties has increased from 1,500 to 3,300 kg/ha while in non-FCV varieties, the yield increased from 2,000 to 4,000 kg/ha. Beside the yield, cured leaf quality was also substantially improved with the release of new varieties. Efforts are on to breed tobacco varieties suitable for extraction of edible oil besides higher levels of phyto-chemicals viz., solanesol, nicotine, protein etc.</p> 
                <p>
                This Division is instrumental in producing and distributing the pure seed of cultivated tobacco varieties to farmers for raising uniform healthy crop. In any given season, more than 90% of the tobacco crop area is planted with the tobacco seed supplied by ICAR-CTRI and its Regional Stations. The service rendered by the Institute, to the farming community in seed production aspects is paying richer dividends in terms of increased leaf quality, higher leaf yields and greater monetary returns to the growers.</p>
                <p>
                As a National Active Germplasm Site, ICAR-CTRI is giving top priority for the tobacco germplasm management. At present more than 3,300 germplasm accessions belonging to N. tabacum L and N. rustica L besides 60 wild Nicotiana species, are maintained in the Institute gene bank. Germplasm collection, maintenance, evaluation, documentation and utilization are taken up on regular basis. Germplasm accessions with desirable agronomic and quality traits and resistance to various diseases and insect pests are identified and are being exploited in varietal improvement programmes.</p>


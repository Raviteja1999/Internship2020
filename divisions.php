                    <h3 style="text-align: left; "><span style="color:#b22222;">SEED PRODUCTION SECTION</span></h3>
                    
                <p>
                    Tobacco is an important commercial crop cultivated in 0.45 million ha producing annually 800 million kg of leaf.  Pure seed place a vital role in obtaining uniform crop growth which inturn ensures higher yields and superior leaf quality in tobacco. In order to provide quality seed to tobacco farmers, ICAR-CTRI, Rajahmundry started producing and distributing the pure seed of released tobacco varieties to the farmers since 1952.  Further, Indian Council of Agricultural Research (ICAR), New Delhi has sanctioned a Revolving Fund Scheme (RF Scheme) in the year 1990 with the objective of supplying pure seed and healthy seedlings of released tobacco varieties to growers for improving the yield and quality of Flue-Cured-Virginia (FCV) tobacco. Under the Scheme, annually about 8,000 kg of FCV tobacco seed and 100 kg of Lanka tobacco seed (Non-FCV) are produced and distributed to the tobacco growers in Andhra Pradesh. Quality seeds of released varieties are produced following the standard seed production practices at Institute farm or at selected farmer fields. The pure seed confirming to the seed standards are distributes to tobacco growers with ICAR-CTRI security Hologram fixed on the sachets in order to make them tamper proof. </p>

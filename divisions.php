                    <strong><span style="color:#b22222;">DIVISION OF CROP CHEMISTRY AND SOIL SCIENCE</span></strong></p>
                    
                    <p>
                    The Division of Crop Chemistry and Soil Science has been entrusted with the responsibility to conduct the basic, strategic and applied research related to assessment and management of soil resource constraints for production efficiency and product quality. The priority areas of research include, soil quality management for tobacco productivity and quality, enhancing nutrient use efficiency, nutrient supplementation through organic inputs and bio-fertilizers, tobacco responses to abiotic stresses and their alleviation, development of flavour profiles and reduction of harmful substances in tobacco, and evaluation and exploitation of tobacco for alternative uses with special focus on seed oil and phyto-chemicals (solanesol, nicotine, leaf protein etc.) having potential uses in food and drug industry. This division also initiated research efforts on utilisation of solar energy and agri biomasses for curing of FCV tobacco to save the wood fuel.</p>

                <p>
                    The Division is equipped with the state-of-the-art laboratory facilities to backup research activities. It houses sophisticated instruments like Total Organic Carbon (TOC) Analyzer, Autoanalyser, UV-Vis Spectrophotometer, Atomic Absorption Spectrophotometer, Photosynthetic System, Plant Canopy Analyzer, Chlorophyll Meter, Gel Documentation System, PCR Analyser, GC, GC-MS, GC-TEA, HPLC, Cigarette Smoking Machine, Zeiss Microscope and other routinely used laboratory paraphernalia. </p>

                <p>
                    The Division has five specialized constituent units to render services relating to Soil and Water testing, Tobacco Leaf Quality Evaluation, Pesticide Residue Analysis, Cigarette Testing and Smoke Analysis, and Seed Testing. The Division is manned with scientific and technical staff having expertise and experience to provide training and consultancy in the specific areas including soil testing and water quality assessment, tobacco leaf quality evaluation, pesticide residue analysis, cigarette and smoke testing, seed testing and tobacco curing technologies.</p>

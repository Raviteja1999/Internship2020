    Morphological description of N. tabacum and N. rustica, the two species of commercial importance are given below. </p>
    <p>
    Significant variation exits in the genus Nicotiana for plant habit, height and morphology of its leaves (shape, thickness and size, form of tip, petiole/sessile, leaf angle to stem, leaf asymmetry etc.). Tobacco plant possesses an extensive but comparatively shallow fibrous root system. Most of these roots develop adventitiously from the portion of the main stem burried during transplanting.  Both N. tabacum and N. rustica have got an erect, sturdy or more or less woody stem. Leaves are formed spirally, evenly spaced and show heteroblastic development.  The leaves of N. rustica are usually petiolate and of regular ovate or cordate shape with a dark green, shiny surface. N. tabacum leaves are mostly sessile, ovate or oblong-lanceolate shaped. N. tabacum types are taller than rustica types. In most of the varieties, the leaf petiole is winged (auricled). </p>
    <p>Tobacco is normally self-pollinated, although as much as 4 to 10 per cent of cross-pollination occurs from pollen carried by insects. Most of the tobacco varieties flower between 55-80 days after planting.</p>
      
    <p>The inflorescence of N. tabacum is terminal raceme, which may bear as many as 150 flowers. Tobacco flower is perfect and consists of a corolla, with a pistil and stamens enclosed within it. The corolla contains five petals which are fused into a long tube and which terminate in five expanded lobes at the top. Corolla outer surface is pale greenish cream in colour, upper half similar in colour or pink to red.  The flower bears five stamens, which are fused to the corolla tube, and a pistil with a long slender style and a blunt two-lobed stigma. The stigma is generally sticky and pollen adheres to it readily. Capsule is narrow, elliptic, ovoid or orbicular, acute or blunt and 15-18 mm long. Seed is nearly spherical or elliptic and light brown. </p>
    <p>
    In N. rustica, the inflorescence is usually a compact <q>thyrse</q> represented by a thickened central axis, shortened branches and monopodial-sympodial development of peripheral flowers. Corolla is yellow to greenish yellow in colour. Throat is broadly obconic with contraction near the mouth. Stamens are white, with four of them long and one short. Capsule is elliptic, ovoid or sub-globose, 7-15 mm long. Seeds are elliptic, oval and dusky brown. </p>
    
    <p>The seeds of tobacco are extremely small and are usually borne in a two-valved capsule. A single tabacum capsule may yield from 2,000 to 5,000 very small seeds and hence, a single plant may produce several hundred thousand seeds. Plants of rustica yield about one-fourth of this number of seeds. </p>
    
        

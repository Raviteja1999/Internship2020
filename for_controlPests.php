	 <h1 style="text-align: center; "><span style="font-size:18px;"><span style="color: rgb(165, 42, 42); "><strong>MANAGEMENT OF INSECT PESTS</strong></span></span></h1>
    
    <h5 style="">
        <span style="color:#800000;"><strong><span style="font-size: 18px; ">Pests in tobacco nursery</span></strong></span></h5>
    
    <p>
        The pests that infest in the nursery are</p>
    <p>
        (1) Caterpillars and Cut-worms (2) Stem borer and (3) Whitefly<br />
        <br />
        <span style="color:#800000;"><b>Caterpillars &amp; Cutworms</b></span></p>
    <p style="line-height:1.3;">
        Four species of Caterpillars viz.,<i>Spodoptera litura</i> F., <i>Laphygma exigua</i> HB., <i>Agrotis ypsilon</i> Rott. and <i>Plusia signata</i> F. are known to infest tobacco nurseries. Of these 4 species <i>Spodoptera litura</i> F. commonly known as tobacco caterpillar is the most important pest.</p>
    <p>
        <span style="color:#800000;"><b><u>Tobacco Caterpillar : <i>Spodoptera litura</i> F</u></b></span></p>
    <table>
        <tbody>
            <tr>
                <td>
                    <img height="110" src="img/ff_pests1.jpg" width="155"  alt="Pests1" /></td>
                <td class="tabletext">
                   <i> The young caterpillars are light green with black head or black spots and mine on the leaf tissues. The well grown caterpillars are grey or dark brown with `V&#39; shaped white mark on the front portion of the black head. They feed voraciously along the veins of leaves and also cut the stems of small and tender seedlings. Hence they are also known as cut worms. There will be about 80 to 100% loss due to this pest.</i></td>
            </tr>
        </tbody>
    </table>
    <p>
        <span style="color:#800000;"><strong>C</strong><b>ontrol Measures</b></span></p>
    <ul>
        <li>
            <p>
                Deep ploughing of nursery area during April-May will facilitate the exposure of pupae to high temperature and thus destroy them.</p>
        </li>
        <li>
            <p> Sowing of Castor around tobacco nursery 15 days before sowing of tobacco so that the castor puts up sufficient foliage by the time <i>Spodoptera</i> infestation builds up. The castor seedlings attract female moths of <i>Spodoptera</i> for egg laying. Leaves having egg masses and tiny caterpillars are clipped and destroyed.
    
    </p>
        </li>
        <li>
            <p>
            Four `litlure&#39; pheromone traps are installed (loaded with 2.5 mg pheromone per vial) per acre two weeks after germination. If the catch of male <i>Spodoptera</i> moths is more than 5 per trap 1 % neem seed kernel suspension (NSKS) in water on 3 week old seedlings and 2% NSKS on 4 week old seedlings is to be sprayed.<br />
                &nbsp;</p>
        </li>
    </ul>
    <p>
        For preparing 10 litres of 1% NSKS 100 gm of neem seed kernel powder is taken and placed in a small muslin cloth bag.</p>
    <p>
        The bag is dipped in a bucket (or any vessel) containing 10 litres of water and the bag is squeezed for 15 minutes to obtain a light brownish liquid. This suspension can be directly sprayed on tobacco seedlings.</p>
    <p>
        For preparing 2% NSKS of 10 litres, 200 gm of neem seed kernel powder is to be taken and prepared as above.</p>
    <p>
        Spraying 50 litres of 1% NSKS on 3 week old seedlings and 60 litres of 2% NSKS on 4 week old seedlings or 1125 litres of nuclear polyhedrosis virus (NPV) solution prepared by 250 NPV infected larva by mixing with 250 g rice powder or starch powder during evening times or <i>Bacillus thuringiensis</i> var. <i>kurstaki</i> @ 1.0 kg in 1125 litres of water per hectare controls the pest.</p>
    <p>
        If the number of seedlings damaged by <i>Spodoptera</i> exceeds 6 per sq.m. area spraying any one of the following insecticides from 4 weeks onwards is recommended.</p>
    <p>
        (a) Emamectin benzoate 5 SG @ 5 g in 10 litres of water</p>
    <p>
        (b) Novaluron 10 EC @ 10 ml in 10 litres of water.</p>
    <p  style="line-height:1.4;">
        During cyclonic weather, tobacco caterpillar, <i>S. litura</i> can also be controlled by putting jaggery and rice bran baits. This is prepared by dissolving 6.25 kg jaggery in 12.5 litres of water and adding to this emamectin benzoate 5 SG 200 ml. To this 25 kg rice bran is slowly added by mixing it thoroughly with a smooth stick so that the bait is made into small balls. The bait is broadcasted on the nursery beds during evening hours to effectively control the caterpillars.</p>
    <p>
        The other 3 species of caterpillars viz., (1) green caterpillar, <i>L. exigua</i> (2) green looper <i>P. signata</i> and (3) black caterpillar, <i>A. ypsilon</i> that sometimes infest the tobacco nurseries can also be controlled by spraying any one of the above insecticides.</p>
    <p>
        <span style="color:#800000;"><b>Stem borer</b></span></p>
    <table>
        <tbody>
            <tr>
                <td style="width:95px;">
                    <img height="110" src="img/ff_pests2.jpg" width="130" alt="pests2" /></td>
                <td class="tabletext" style= "width:459%;">
                   <i> The stem borer is the larva of a tiny brown moth called <i>Scrobipalpa (Gnorimoschema) heliopa</i> Low. The tiny caterpillars bore inside the stem and midribs and feed on internal tissues. As a result swelling appears where the borer stays.Borer infested seedlings when planted in the field remain stunted and some times unusual  branching of the plant is seen.</i></td>
            </tr>
        </tbody>
    </table>
    <p>
        <span style="color:#800000;"><strong>C</strong><b>ontrol Measures</b></span></p>
    <ul>
        <li>
            <p>
                Stem borer affected seedlings should be removed and destroyed.</p>
        </li>
        <li>
            <p>
                In tobacco nurseries when stem borer is a serious problem, spraying of Flubendiamide 48 SC @ 2.5 ml or chlorantriniliprole 18.5 SC @ 3 ml in 10 litres of water at 40 and 55 days after germination is taken up. Final spray of one of these insecticides should be given before pulling seedlings.</p>
        </li>
        <li>
            <p>
                In planted crop above insecticide is sprayed twice or thrice at 10 days interval starting from 15 days after planting. After completion of harvestings the stem borer affected plants are uprooted and burnt to prevent carry over of the pest to the next season.<br />
                &nbsp;</p>
        </li>
    </ul>
    <p>
        <span style="color:#800000;"><b>Whitefly:<i> Bemisia tabaci</i> Gennadius</b></span></p>
    <p>
        White flies are small fly like insects seen on the underside of leaves. They will fly immediately when the plant is disturbed. The adult fly sucks the sap from leaves and transmits the leaf curl virus disease from infected to healthy seedlings.</p>
    <p>
        <b style="text-color:black">Control Measures</b></p>
    <ul>
        <li>
            <p>
                Alternate weed hosts around nursery area are removed and destroyed.</p>
        </li>
        <li>
            <p>
                Yellow-sticky traps (20 cm x 15 cm size galvanized iron sheet painted with yellow colour coated with castor oil) are installed @ 5 per acre.</p>
        </li>
        <li>
            <p>
                If the population of whitefly is 100 per each sticky trap, the following insecticides may be sprayed at 10 days interval commencing from 4 weeks after germination.</p>
        </li>
        <li>
            <p>
            Imidacloprid 200 S.L. @ 2.5 ml in 10 litres of water (or)</p>
        </li>
        <li>
            <p>
                Thiamethoxam 25 WG @ 2gm (or) pymetrozine 50 WG @ 4 g (or) flonicamid 50 WG @ 4g in 10 lit of water &nbsp;</p>
        </li>
    </ul>
    <p>
        <span style="color:#800000;"><b>PESTS IN THE PLANTED CROP</b></span></p>
    <p>
        The major pests that affect tobacco crop in the field are whitefly, leaf eating caterpillars, tobacco aphid/green peach aphid and capsule borer. Control measures for these major pests are detailed below.</p>
    <p>
        <span style="color:#800000;"><strong>Leaf eating caterpillar :<i> Spodoptera litura</i> F.</strong></span></p>
    <p style="line-height:1.4;">
        In the field crop the pest incidence is generally confined to few border rows. The female moth lays about 2000 eggs on the either side of the leaves in bunches. After 2-3 days of incubation light green tiny caterpillars with black head emerge in hundreds. They eat the green matter of the leaf and turn to grey or dark grey or black in colour. The grown up larva feed voraciously on the tobacco leaf making lot of holes on the leaf causing damage to an extent of 10 to 15%.</p>
    <p  style="line-height:1.4;">
        Ten pheromone traps per ha are installed 20 days after planting. The leaf is critically examined for egg masses and tiny caterpillars which are later collected and destroyed. If the infestation is severe, the hot spots are sprayed thoroughly with 0.5% neem seed kernel suspension or nuclear polyhedrosis virus 250 LE in 1125 litres of water per ha by mixing with 250g rice powder or starch powder or any one of the following insecticides.</p>
    <p>
        a. Emamectin benzoate 5 SG @ 5 g in 10 litres of water.</p>
    <p>
        b. Novaluron 10 EC @ 10 ml in 10 litres of water</p>
    <p>
        <span style="color:#800000;"><b>Whitefly :<i> Bemisia tabaci</i> Gennadius</b></span></p>
    <p>
        These tiny white flies cause leaf curl disease. The leaves of curled plants are twisted, puckered and thickened with abnormally prominent veins. The plants show stunted growth and the yields are reduced.</p>
    <p>
        <span style="color:#800000;"><b>Control Measures</b></span></p>
    <ul>
        <li>
            <p>
                Leaf curl affected seedlings are to be avoided while transplanting in the field.</p>
        </li>
        <li>
            <p>
                Alternate weed hosts are to be removed and destroyed.</p>
        </li>
        <li>
            <p>
                Crops like brinjal and sunflower should not be grown in the vicinity of tobacco fields.</p>
        </li>
        <li>
            <p>
                If the population of leaf curl infested plants is less than 2% within one month after planting, they are removed and destroyed.</p>
        </li>
        <li>
            <p>
            Twelve yellow sticky (castor oil coated) traps per hectare are installed to monitor the white fly population.</p>
        </li>
        <li>
            <p>
            If the whitefly population is heavy the spray schedule recommended for the nursery has to be adopted in the field crop also commencing from 10 days after planting at an interval of 15. <br />
                &nbsp;</p>
        </li>
    </ul>
    <p  style="line-height:1.4;" >
        Spraying is done preferably in the evening hours (4 to 6 pm) with high volume sprayers ensuring that the both sides of leaves are covered. Further, the spraying schedule has to be adopted by all the tobacco farming community in the endemic area. Spraying should be given 10 days after planting.</p>
    <p>
        <span style="color:#800000;"><b>Tobacco aphid :<i> Myzus nicotianae</i> Blackman</b></span></p>
    <p>
        <span style="color:#800000;"><b>Green peach aphid:<i> Myzus persicae</i> Sulz:</b></span></p>
    <p  style="line-height:1.4;">
        Aphids are louse like insects which are green or pinkish to brown in colour. In case of heavy infestation hundreds of them can be seen on the underside of leaves. By constantly sucking the sap from leaves they make the plant pale and sickly and thereby retard the growth. They secrete sugary juice known as `honey dew&#39; on the leaves due to which sooty mould develops rendering the leaves unfit for curing. In addition they also transmit virus diseases like rosette or bushy top.</p>
    <p>
        <span style="color:#800000;"><b>Control Measures</b></span></p>
    <p>
        During early winter, for control of aphids when 2% of the plants are infected, any one of the following insecticides is sprayed once.</p>
    <p>
        <b style="font-color:black;">or</b></p>
    <ul>
        <li>
            Imidacloprid 200 SL @ 2.5ml (or) thiamethoxam 25 WG @ 2gm (or) pymetrozine 50 WG @ 4 g (or) flonicamid 50 WG @ 4g  in 10 litres of water<br />
            &nbsp;</li>
        
    </ul>

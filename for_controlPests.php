    <p>
        <span style="color:#800000;"><b>Capsule borer :<i> Heliothis armigera</i> Hb</b></span></p>
    <p>
        The larvae of this species are greenish or pale brown with broken dark-brown stripes along the body. They bore the capsules and feed on the seeds inside. They feed on top leaves also when the infestation is heavy.</p>
    <table>
        <tbody>
            <tr>
                <td>
                    <img height="111" src="img/ff_pests5.jpg" width="155" alt="pests5" /></td>
                <td class="tabletext">
                    <i>The incidence of capsule borer is observed during grand growth phase of the crop under favourable conditions, preferably after heavy rains in untopped fields. The incidence is seen from 30 days after planting. Generally one larva is seen on the terminal bud. Between  30 - 50 days it feeds on the terminal bud and then on the young leaves and causes loss to the crop. More than one borer per plant are seen after the flowering and then it feeds on the developing seed of the capsules.</i></td>
            </tr>
        </tbody>
    </table>
    <p>
        &nbsp;</p>
    <p>
        <span style="color:#800000;"><b>Control measures</b></span></p>
    <ul>
        <li>
            <p>
                Hand picking of the bud worm/capsule borers in the tobacco crop once in three to four days and destroying. Care should be taken to avoid touching the TMV infected plants.</p>
        </li>
        <li>
            <p>
            Flower heads are topped or trimmed to bring down the incidence of capsule borer considerably.</p>
        </li>
        <li>
            <p>
                In untopped fields flubendiamide 48 SC @ 2.5ml or chlorantranaliprole 18.5 SC @ 3 ml in 10 liters of water or nuclear polyhedrosis virus (NPV) of Heliothis @ 250 LE or Bacillus thuringiensis var. kurstaki @ 1.5 kg in 1125 litres of water per hectare is sprayed. For NPV solution strach power @ 250 mg per ha is added and sprayed in evening hours.<br />
                &nbsp;</p>
        </li>
    </ul>
